mascotas = ["pea", "aaa", "asdad"]
# con count es para saber cuantos elementos del mismo elemento
print(mascotas.count("aaa"))
if "aaa" in mascotas:  # comprueba si existe elemento en la lista
    # para buscar un elemento dentro de la lista y si existee arroja el index del elemento en la lista digamos su numero
    print(mascotas.index("aaa"))
