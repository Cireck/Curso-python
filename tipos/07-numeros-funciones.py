import math  # se  importa un modulo para realizar mas operacion con numeros
print(round(1.3))  # es para redodear el numero
print(abs(-77))  # es para obtener el valor absoluto
# ceil es para obtener el numero superior o hacia arriba mas cercano entero del numero
print(math.ceil(1.1))

# floor  es para obtener el numero mas abajo o hacia abajo mas cercano  entero del numero
print(math.floor(1.1))
# isnan es para validar si es un numero pero no se puede usar con string
print(math.isnan(23))
# pow(elvacion del numero,numero que se va hacer la elevacion) es para elevar un numero hasta un numero senalado
print(math.pow(10, 3))
print(math.sqrt(9))#sqrt es para obtener la raiz cuadrada de un numero 
